#include <unistd.h>

void	ft_putchar(int i, int j, int x, int y)
{
	if (j == 1)
	{
		if ((i == 1) || (i == x))
			write(1, "A", 1);
		else
			write(1, "B", 1);
	}
	else if (j == y)
	{
		if ((i == 1) || (i == x))
			write(1, "C", 1);
		else
			write(1, "B", 1);
	}
	else if (i == 1 || i == x)
		write(1, "B", 1);
	else
		write(1, " ", 1);
}
