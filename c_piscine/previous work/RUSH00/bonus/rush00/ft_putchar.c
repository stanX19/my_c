#include <unistd.h>

void	ft_putchar(int i, int j, int x, int y)
{
	if (i == 1 || i == x)
	{
		if (j == 1 || j == y)
			write(1, "o", 1);
		else
			write(1, "|", 1);
	}
	else if (j == 1 || j == y)
		write(1, "-", 1);
	else
		write(1, " ", 1);
}
