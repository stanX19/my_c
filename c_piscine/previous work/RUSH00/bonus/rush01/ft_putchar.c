#include <unistd.h>

void	ft_putchar(int i, int j, int x, int y)
{
	if (i == 1 || j == 1)
	{
		if ((i == x && x != 1) || (j == y && y != 1))
			write(1, "\\", 1);
		else
			write(1, "*", 1);
	}
	else if (x == i || y == j)
	{
		if (x == i && y == j)
			write(1, "/", 1);
		else
			write(1, "*", 1);
	}
	else
		write(1, " ", 1);
}
