#include <unistd.h>

int	ft_str_is_alpha(char *str)
{
	int		i;
	char	x;

	i = 0;
	while (str[i])
	{
		x = str[i];
		if (x < 65 || x > 122
			|| (x > 90 && x < 97))
			return (0);
		i++;
	}
	return (1);
}
