#include <unistd.h>

unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	unsigned int	i;

	i = 0;
	while (src[i])
	{
		if (i < size - 1)
			dest[i] = src[i];
		else if (i == i - 1)
			dest[i] = 0;
		i++;
	}
	if (i < size)
		dest[i] = 0;
	return (i);
}
