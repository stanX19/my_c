#include <unistd.h>

int	ft_strcmp(char *a, char *b);

int	ft_strcmp(char *a, char *b)
{
	while (*a != '\0' || *b != '\0')
	{
		if (*a < *b)
			return (-1);
		else if (*a > *b)
			return (1);
		a++;
		b++;
	}
	if ((*a == '\0') && (*b == '\0'))
		return (0);
	else if ((*a == '\0') && (*b != '\0'))
		return (-1);
	else
		return (1);
}
